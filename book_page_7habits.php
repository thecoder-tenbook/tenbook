                <h3>Головна метафора: зсередини назовні</h3>
                <p>Більшість книг з саморозвитку вчать, як підстригати гілки — змінювати поведінку, говорити правильні слова. Результат тимчасовий. Кові пропонує зайнятися корінням — характером, системою цінностей, тим, ким ви насправді є.</p>
                <p>Кові розрізняє «Етику Характеру» і «Етику Особистості». Перші 200 років американської літератури з саморозвитку були про характер: чесність, скромність, стійкість. Після Першої світової відбувся зсув до іміджу й харизми. Саме цей зсув, стверджує Кові, є причиною більшості особистісних і управлінських криз.</p>
                <h3>Модель зрілості</h3>
                <p>Книга структурована навколо руху від залежності через незалежність до усвідомленої взаємозалежності. Залежна людина думає: «Ти маєш подбати про мене». Незалежна: «Я можу подбати про себе сам». Взаємозалежна: «Разом ми досягнемо того, чого жоден не досягне поодинці».</p>
                <p>Перші три звички формують внутрішній характер і особисту незалежність. Звички 4–6 будують продуктивні стосунки й взаємозалежність. Сьома підтримує і поглиблює всі попередні.</p>
