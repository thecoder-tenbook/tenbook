let currentChapter = 0;
let fontSize = 17;
let readChapters = new Set([0]);

function renderChapter(idx) {
    const ch = chapters[idx];
    const isLast = idx === chapters.length - 1;
    const isFirst = idx === 0;
    const prevCh = !isFirst ? chapters[idx - 1] : null;
    const nextCh = !isLast ? chapters[idx + 1] : null;

    document.getElementById('readerContent').innerHTML = `
        <div class="chapter-label">${ch.label}</div>
        <div class="chapter-title">${ch.title}</div>
        <div class="chapter-meta">
            <span class="chapter-time">
                <svg width="13" height="13" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"><circle cx="12" cy="12" r="10"/><polyline points="12 6 12 12 16 14"/></svg>
                ${ch.time}
            </span>
        </div>
        <div class="reader-body">${ch.content}</div>
        <div class="chapter-nav">
            ${prevCh ? `<button class="nav-prev" onclick="goToChapter(${idx-1})">
                <span class="nav-direction"><svg width="12" height="12" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2.5" stroke-linecap="round" stroke-linejoin="round"><polyline points="15 18 9 12 15 6"/></svg> Попередній</span>
                <span class="nav-chapter-title">${prevCh.title}</span>
            </button>` : '<div></div>'}
            <span class="nav-center">${idx + 1} / ${chapters.length}</span>
            ${nextCh ? `<button class="nav-next" onclick="goToChapter(${idx+1})">
                <span class="nav-direction">Наступний <svg width="12" height="12" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2.5" stroke-linecap="round" stroke-linejoin="round"><polyline points="9 18 15 12 9 6"/></svg></span>
                <span class="nav-chapter-title">${nextCh.title}</span>
            </button>` : `<button class="nav-next" onclick="window.location='book_page_7habits'">
                <span class="nav-direction">Готово <svg width="12" height="12" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2.5" stroke-linecap="round" stroke-linejoin="round"><polyline points="9 18 15 12 9 6"/></svg></span>
                <span class="nav-chapter-title">Повернутися до книги</span>
            </button>`}
        </div>
    `;

    document.querySelector('.topbar-title').textContent = `7 Звичок · ${ch.label}`;

    document.querySelectorAll('.toc-item').forEach((item, i) => {
        item.classList.toggle('active', i === idx);
    });

    readChapters.add(idx);
    const pct = Math.round((readChapters.size / chapters.length) * 100);
    document.getElementById('progressFill').style.width = pct + '%';

    window.scrollTo({ top: 0, behavior: 'smooth' });
    currentChapter = idx;
    closeTocMobile();
    saveProgress();
}

function goToChapter(idx) { renderChapter(idx); }

function toggleSettings() {
    document.getElementById('settingsPanel').classList.toggle('open');
}

function toggleToc() {
    document.getElementById('tocSidebar').classList.toggle('open');
    document.getElementById('tocOverlay').classList.toggle('open');
}

function closeTocMobile() {
    if (window.innerWidth <= 768) {
        document.getElementById('tocSidebar').classList.remove('open');
        document.getElementById('tocOverlay').classList.remove('open');
    }
}

document.addEventListener('click', (e) => {
    const panel = document.getElementById('settingsPanel');
    const btn = document.getElementById('settingsToggle');
    if (panel.classList.contains('open') && !panel.contains(e.target) && !btn.contains(e.target)) {
        panel.classList.remove('open');
    }
});

function changeFontSize(delta) {
    fontSize = Math.max(14, Math.min(22, fontSize + delta));
    document.documentElement.style.setProperty('--font-size', fontSize + 'px');
    document.getElementById('fontSizeVal').textContent = fontSize + 'px';
}
